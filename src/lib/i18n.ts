export const getTranslation = (lang: Language, key: string, defaultValue: string = key): string => {
  return translations[lang]?.[key] || translations['en'][key] || defaultValue;
};

export const getCurrentLanguage = (): Language => {
  try {
    const stored = localStorage.getItem('veer.language');
    if (stored && languages.find(l => l.code === stored)) return stored as Language;
  } catch (e) { void e; }
  return 'en';
};

export const setCurrentLanguage = (lang: Language): void => {
  try {
    localStorage.setItem('veer.language', lang);
    window.dispatchEvent(new CustomEvent('veer-language-change', { detail: lang }));
  } catch (e) { void e; }
};
