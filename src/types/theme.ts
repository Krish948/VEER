// Theme types and presets for VEER

export interface ThemeColors {
  // Base colors (HSL values without 'hsl()' wrapper)
  background: string;
  foreground: string;
  
  // Glass effect
  glassBg: string;
  glassBorder: string;
  
  // Card
  card: string;
  cardForeground: string;
  
  // Popover
  popover: string;
  popoverForeground: string;
  
  // Primary accent
  primary: string;
  primaryForeground: string;
  primaryGlow: string;
  
  // Secondary accent
  secondary: string;
  secondaryForeground: string;
  secondaryGlow: string;
  
  // Tertiary accent
  accent: string;
  accentForeground: string;
  accentGlow: string;
  
  // Muted
  muted: string;
  mutedForeground: string;
  
  // Destructive
  destructive: string;
  destructiveForeground: string;
  
  // Border & Input
  border: string;
  input: string;
  ring: string;
  
  // Sidebar
  sidebarBackground: string;
  sidebarForeground: string;
  sidebarPrimary: string;
  sidebarPrimaryForeground: string;
  sidebarAccent: string;
  sidebarAccentForeground: string;
  sidebarBorder: string;
  sidebarRing: string;
}

export interface Theme {
  id: string;
  name: string;
  description: string;
  colors: ThemeColors;
  isDark: boolean;
}

